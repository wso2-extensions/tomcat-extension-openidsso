package org.wso2.carbon.tomcat.oidcsso.extension.bean;

/**
 * A Java class which models a holder for the parameters of Token Response
 */
public class TokenResponse {
    private String accessToken;
    private String refreshToken;
    private String idToken;
    private String tokenType;
    private String expiresIn;
    private String idTokenClaimSet;

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getRefreshToken() {
        return refreshToken;
    }

    public void setRefreshToken(String refreshToken) {
        this.refreshToken = refreshToken;
    }

    public String getIdToken() {
        return idToken;
    }

    public void setIdToken(String idToken) {
        this.idToken = idToken;
    }

    public String getTokenType() {
        return tokenType;
    }

    public void setTokenType(String tokenType) {
        this.tokenType = tokenType;
    }

    public String getExpiresIn() {
        return expiresIn;
    }

    public void setExpiresIn(String expiresIn) {
        this.expiresIn = expiresIn;
    }

    public String getIdTokenClaimSet() {
        return idTokenClaimSet;
    }

    public void setIdTokenClaimSet(String idTokenClaimSet) {
        this.idTokenClaimSet = idTokenClaimSet;
    }
}
